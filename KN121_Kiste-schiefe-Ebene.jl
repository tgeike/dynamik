md"""**Hinweis** Der Index F in den Variablen (z. B. `sF`, `tF` steht für den Beginn des Federanschlags)"""

# ╔═╡ bd62df7a-2420-4360-ad17-c3b66af46154
md"""Beim Herunterrutschen der schiefen Ebene wirken auf die Kiste die Gewichtskraft und die Reibungskraft. Im zweiten Streckenabschnitt (ab Erreichen des Anschlags) wirkt zusätzlich die Federkraft. Beim Aufstellen der Bewegungsgleichung kann der zweite Streckenabschnitt herangezogen werden. Für den ersten Streckenabschnitt erhält man die Bewegungsgleichung, indem die Federsteifigkeit zu Null gesetzt wird. Durch Freischneiden wird die Reaktionskraft (Zwangskraft) zwischen Kiste und Rutschbahn sichtbar. Diese Kraft wird Normalkraft oder Kontaktkraft genannt und ist in Richtung der Flächennormale der Rutschbahn gerichtet. Wir wählen den Richtungssinn so, dass die Normalkraft nach oben auf die Kiste wirkt. Der Wert der Normalkraft ist dann positiv, solange die Kiste mit der Bahn in Kontakt ist."""

# ╔═╡ 8e06c181-d860-4d32-8b78-edee6853f270
md"""
Wir unterstellen, dass die Kiste nicht kippelt, sondern rein translatorisch die Ebene herunterrutscht.

**Kräftegleichung in Bewegungsrichtung**
```math
m \ddot s = - F_\mathrm{R} - F_\mathrm{F} + F_\mathrm{G}\sin\alpha
```
**Kräftegleichung senkrecht zur Bewegungsrichtung** (Richtung der Flächennormale der Rutschbahn)
```math
0 =  F_\mathrm{N} - F_\mathrm{G}\cos\alpha
```
Mit dem konstitutiven Gesetz ``F_\mathrm{R} = \mu F_\mathrm{N}`` für die kinetische Reibungskraft (Gleiten), dem konstitutiven Gesetz ``F_\mathrm{F} = c(s-s_\mathrm{F})`` für die Feder und der Gewichtskraft gemäß ``F_\mathrm{G} = mg`` folgt die Bewegungsgleichung im ersten Streckenabschnitt (keine Feder)
```math
\ddot s = g(\sin\alpha -\mu\cos\alpha)
```
und im zweiten Streckenabschnitt (mit Feder)
```math
\ddot s = g(\sin\alpha -\mu\cos\alpha) - \frac{c}{m}(s-s_\mathrm{F})
```
"""

# ╔═╡ b5a64ea3-8c82-421c-bc0c-62cb4bd623b2
md"""
Es werden **zwei** Lösungswege aufgezeigt.

1. Analytische Herleitung der Lösung mit Stift und Papier und Übertragung der fertigen Gleichungen in den Julia-Code

2. Numerisches Lösen der Bewegungsgleichungen (AWP)

Wir wollen im ersten Schritt bewusst nicht die Bewegungsgleichungen numerisch integrieren, sondern lediglich händisch hergeleitete Formeln auswerten und visualisieren.
Alle Parameter sind mit Einheiten angegeben und werden mit diesen in den folgenden Rechnungen benutzt. Probieren Sie aus was passiert, wenn Sie unzulässige Einheiten verwenden, beispielsweise für die Federsteifigkeit die Einheit Newton statt Newton je Meter.

Erst im zweiten Schritt wird die Bewegungsgleichung numerisch gelöst."""

# ╔═╡ f18a6831-2af2-46e0-bae7-509db64a025d
md"""Parameter mit Einheiten"""

# ╔═╡ 947e92ab-bfb3-464d-9db9-8f56c51996c2
begin 
	α = 30.0u"°"
	m = 10.0u"kg"
	g = 9.81u"m/s^2"
	c = 100.0u"N/m"
	μ = 0.1
	sF = 2.0u"m"
end;

# ╔═╡ 34c5c8fa-7952-4839-b8b1-8fc7f64c016c
md"""
### Lösung 1: analytische Formeln für die Lösung
#### Abschnitt I: vor Berühren des Anschlags

Vor Berühren des Anschlags ist die Beschleunigung konstant und ergibt sich aus den Effekten Gewichtskraft und Reibungskraft."""

# ╔═╡ aad6b62e-5481-4d8c-92df-4cda280f2792
aI = g*(sin(α) - μ*cos(α))

# ╔═╡ 70d92960-e70d-401c-87ce-ff723ce6c454
md"""Wir möchten die Geschwindigkeit als Funktion des Weges darstellen. Die gesuchte Funktion ``v(s)`` für Abschnitt I kann auf verschiedenen Wegen ohne größere Schwierigkeiten aus der bekannten Beschleunigung bestimmt werden."""

# ╔═╡ e8d8aad1-0274-4733-8957-7093bf075918
vI(s) = sqrt(2*aI*s);

# ╔═╡ e5a7ebc9-0737-4259-a864-0a89e3bbceea
vI(sF)

# ╔═╡ cfccf62c-b687-47ab-b176-bc78f2a5c5d6
md"""Bei Erreichen des Anschlags beträgt die Geschwindigkeit $(round(typeof(1.0u"m/s"),vI(sF),digits=2))."""

# ╔═╡ f6d76c76-6f15-4e65-abd6-01d1f67440ae
md"""Zudem kann aus der konstanten Beschleunigung die Zeit berechnet werden, zu der der Anschlag erreicht wird."""

# ╔═╡ d63f81e4-fe50-4da1-8f7e-f855c5a3290c
tF = sqrt(2*sF/aI)

# ╔═╡ a7be2ad4-8469-41eb-b057-a139a46ad6ca
md"""
#### Abschnitt II: nach Berühren des Anschlags bis zum erstmaligen Stillstand"""

# ╔═╡ 1f8a2198-c537-425d-8245-f58feeb4e935
K = aI + c/m*sF

# ╔═╡ 82887c6c-76a4-4e93-852e-90d12b518a93
sB = m*K/c |> u"m"

# ╔═╡ fe747525-a36d-4acc-b157-df93328b46e7
md"""Beim Weg $(round(typeof(1.0u"m"),sB,digits=2)) hat die Beschleunigung einen Nulldurchgang. Ab diesem Weg nimmt die Geschwindigkeit ab."""

# ╔═╡ 9eb500f4-80a2-4225-b33d-aa380b88b9dc
diskr(s) = vI(sF)^2 + 2K*(s - sF) - c/m*(s^2 - sF^2);

# ╔═╡ e3ede9ca-f922-4e41-abd2-dd2a599ecf0f
vII(s) = sqrt(diskr(s));

# ╔═╡ dec5f09c-45dd-4eba-9613-d4c95b4b5204
vII(sB)

# ╔═╡ aa6838d9-fdb6-471c-a0eb-bd9a52bd9a7b
md"""Die maximale Geschwindigkeit beträgt $(round(typeof(1.0u"m/s"),vII(sB),digits=2)) (im Vergleich zu $(round(typeof(1.0u"m/s"),vI(sF),digits=2)) bei Erreichen des Anschlags)."""

# ╔═╡ 5b372a35-5227-4768-9bc9-d412730d48ac
md"""Durch das Zusammendrücken der Feder wird der Körper abgebremst und erreicht einen momentanen Stillstand (danach Bewegungsumkehr). Der bis dahin zurückgelegte Weg kann durch Nullstellensuche bestimmt werden."""

# ╔═╡ 4c8b5474-3eef-4ad3-8c61-44f9a35105a8
smax = find_zero(diskr,3.6u"m")

# ╔═╡ 8ceb0390-aa16-4a57-bfee-40d389fe86ca
md"""Nach $(round(typeof(1.0u"m"),smax,digits=2)) kommt der Körper momentan zur Ruhe. Danach bewegt er sich in die andere Richtung. Der maximale Federweg ist demnach $(round(typeof(1.0u"m"),smax-sF,digits=2))."""

# ╔═╡ d67bdccd-52c9-4794-bfd4-bc3a9864b47d
md"""
#### Grafische Darstellung: Geschwindigkeit-Weg für beide Abschnitte"""

# ╔═╡ c835fe8a-16b6-435d-8743-5b4f30e18627
v(s) = s < sF ? vI(s) : vII(s);

# ╔═╡ cc6858ef-bd5e-4ec7-b25e-53076f6d0852
begin 
	plot(range(0.0u"m",smax,150),v,label=false,lw=2,size=(500,300),xlabel="Weg",ylabel="Geschwindigkeit")
	vline!([sF,sB],label=false)
end

# ╔═╡ 2fcdb15b-c352-48a5-af3b-e46c0aea09e7
md"""Beachten Sie: die maximale Geschwindigkeit des Körpers ist größer als die Geschwindigkeit beim Erreichen des Anschlags. Für den ersten Teil der Strecke II wird der Körper weiter beschleunigt (mit Beschleunigung ``0<a<a_\mathrm{I}``)."""

# ╔═╡ 103dad52-7812-42a7-ba81-92cb3eb3ef3a
md"""
### Lösung 2: Numerische Lösung des AWP
Wir verwenden das Paket `DifferentialEquations` für das Lösen des Anfangswertproblems (AWP).

Für die numerische Lösung werden die Einheiten der Parameter entfernt. Das geschieht so, dass der Zahlenwert der Größe bei vorgegebener Einheit extrahiert wird. Beispielsweise wird bei der Position des Anschlags (Variable `sF`) der Zahlenwert der Größe verwendet, wenn die Einheit Meter ist. Wenn man `sF` oben in Zentimeter oder Inch angeben würde, würde der unten stehende Programmcode korrekt in Meter umrechnen und dann den Zahlenwert zurückliefern. Im Ergebnis werden Längenwerte dementsprechend immer in Metern sein."""

# ╔═╡ aff41cd9-f40f-4974-874c-8808ed8d4be3
struct Systemparameter
	aI::Float64
	χ::Float64
	sF::Float64
end

# ╔═╡ 27a1921f-daeb-44e4-98f9-65569d60b0a6
param = Systemparameter(ustrip(Float64,u"m/s^2",aI), ustrip(Float64,u"s^-2",c/m), ustrip(Float64,u"m",sF))

# ╔═╡ bca822b1-6bef-4456-8aeb-6224fc81c168
function bewegdgl!(F,z,p,t) 
	s = z[1]
	v = z[2]
    F[1] = v
	F[2] = (s < p.sF ? p.aI : p.aI - p.χ*(s-p.sF))
end;

# ╔═╡ cee4a585-d8fc-4f17-af3a-493181de4cc7
md"""Im unten stehenden Code wird ein Zeitintervall angegeben, für das die Berechnung erfolgen soll. Der Integrator bricht ab, wenn vorher die Geschwindigkeit 0 erreicht ist (siehe `condition`)."""

# ╔═╡ f98fc5af-3936-4b4b-9fd0-3384d75e0f0b
begin
	tspan = (0.0,2.0)
	z_start = [0.0 0.0]
	prob = ODEProblem(bewegdgl!,z_start,tspan,param)
	condition(u, t, integrator) = (u[2])
	affect!(integrator) = terminate!(integrator)
	cb = ContinuousCallback(condition, affect!)
    sol = solve(prob, Tsit5(), callback = cb,reltol=1e-8, abstol=1e-8, saveat=0.01)
end

# ╔═╡ b7d8e98c-ce8d-460d-aec5-51eeff25594e
plot(sol,lw=2,xaxis="Zeit [s]",layout=(2,1),title=["Weg [m]" "Geschwindigkeit [m/s]"], label = false)

# ╔═╡ 33cc17ce-2db3-41e5-a1dd-7aa26cb32030
v_analyt(s) = ustrip(Float64,u"m/s",v(s*1.0u"m"));

# ╔═╡ 327e7707-4980-4714-ab3d-a4a81c03515c
plot(sol[1,:],[sol[2,:] v_analyt.(sol[1,:])],lw=[3 1],label=["numerisch" "analytisch"],size=(500,300),xlabel="Weg [m]",ylabel="Geschwindigkeit [m/s]")

