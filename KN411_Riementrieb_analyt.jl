md"""
Analytische Herleitung der Bewegungsgleichung über das Prinzip des kleinsten Zwangs

Das System hat den Freiheitsgrad 1. Demnach genügt eine generalisierte Koordinate für die Beschreibung der Bewegung."""

# ╔═╡ bea44901-955d-4c7d-8c8a-6eaad90a6717
@variables R1,i12,r2,J1,J2,m3,g,TA;

# ╔═╡ 31e7d66b-22dc-4456-835d-67808dc202f0
@variables t ϕ1(t);

# ╔═╡ 6af38487-f765-4c12-b30f-48c1aebdd207
md"""
#### Kinematische Beziehungen aufstellen
Zwischen den generalisierten Koordinaten gelten die folgenden kinematische Beziehungen (bis auf additive Konstanten)."""

# ╔═╡ 01055162-e1a2-45ee-960e-72d9fe8ccfd9
ϕ2 = ϕ1/i12;

# ╔═╡ 049e082b-904d-4014-a593-159f0a90f099
y3 = r2*ϕ2;

# ╔═╡ 7ba23393-7c13-47a1-93d2-6039e1f86894
md"""Aus der Lage folgen durch ein- bzw. zweimaliges Differenzieren die Ausdrücke für die (Winkel-)Geschwindigkeiten und (Winkel-)Beschleunigungen."""

# ╔═╡ fc12ba10-f8e9-4ccf-b059-52c6c0391478
D = Differential(t);

# ╔═╡ 3c849de1-e9d2-44d6-b5ed-276a6c378401
begin
	ω1 = D(ϕ1)
	α1 = D(ω1)
	ω2 = D(ϕ2)
	α2 = D(ω2)
	v3 = D(y3)
	a3 = D(v3)
end;

# ╔═╡ 09bca40f-15a8-41da-ab9e-37c219fc8f73
md"""Später wollen wir den Zwang ``\mathcal{Z}`` nach der generalisierten Beschleunigung ableiten. Dazu definieren wir hier die Ableitung nach ``\alpha_1=\ddot\varphi_1``.""" 


# ╔═╡ 3cb68154-81f1-4022-9aed-7e5fd088a859
Dα1 = Differential(α1);

# ╔═╡ 593d67b1-d9d5-45f6-a54d-e85c8eab1611
md"""
#### Zwang formulieren
Wir schreiben zuerst den Zwang ``\mathcal{Z}`` und ermitteln anschließend durch Ableiten nach der generalisierten Beschleunigung ``\ddot\varphi_1`` die gesuchte Bewegungsgleichung.
"""

# ╔═╡ f067e54e-7221-4e0a-bab8-f2a54200d80d
Z = 1/(2*J1)*(J1*α1 - TA)^2 + J2/2*(α2)^2  + m3/2*(a3 + g)^2

# ╔═╡ c67af4f4-6e13-4328-850f-28a6388b7630
md"""
#### Bewegungsgleichung aus dem Gaußschen Prinzip herleiten
"""

# ╔═╡ 00376350-c088-4870-a0e2-259dc47c49f2
dgl = simplify(expand(expand_derivatives(Dα1(Z))))

